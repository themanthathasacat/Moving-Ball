import turtle


def show_circle(t, color, radius):
    # to fill the color in ball
    t.fillcolor(color)

    # start color filling
    t.begin_fill()

    # draw circle
    t.circle(radius)

    # end color filling
    t.end_fill()

screen = turtle.Screen()

# set screen size
screen.setup(600,600)

# screen background color
screen.bgcolor('purple')

screen.tracer(0)

# create a turtle object object
t = turtle.Turtle()

# set a turtle object color
t.color('yellow')

# set turtle object speed
t.speed(2)

# set turtle object width
t.width(2)

# hide turtle object
t.hideturtle()

#t.penup()

# set initial position
t.goto(-250, 0)

# move turtle object to surface
t.pendown()
t.forward(250)
t.left(90)

t.goto(0, -500)

def move_right():
    t.setheading(0)
    t.forward(25)


def move_up():
    t.setheading(90)
    t.forward(25)
    while True:
        # clear turtle work
        t.clear()

        # call function to draw ball
        show_circle(t, 'yellow', 25)

        # update screen
        screen.update()
        t.forward(0.1)


def move_left():
    t.setheading(180)
    t.forward(25)


def move_down():
    t.setheading(270)
    t.forward(25)

screen.onkeypress(move_right, "Right")
screen.onkeypress(move_up, "Up")
screen.onkeypress(move_left, "Left")
screen.onkeypress(move_down, "Down")
screen.listen()
screen.exitonclick()

# infinite loop
i = 0
step = 0.1
while True:
    # clear turtle work
    t.clear()

    # call function to draw ball
    show_circle(t, 'yellow', 25)

    # update screen
    screen.update()
    t.forward(0.1)
    # forward motion by turtle object
    upper_wall = 5000
    if (i%10000 < upper_wall):
        t.forward(0.1)
    elif (i%10000 == upper_wall):
        t.left(1)
